#!/usr/bin/env node
//
// Merge a translated question-set into an existing target set, injecting
// per-question `translations.<lang> = { q, o }` and (optionally) set-level
// `translations.<lang> = { name, description }`. The target's answers (`a`)
// and Spanish text are preserved verbatim.
//
// Usage:
//   node scripts/merge-translations.js <target.js> \
//     --translation <translation.js> \
//     --lang <code> \
//     [--set-name "<name>"] [--set-description "<desc>"]
//
// Both files must register a single set into `window.QUESTION_SETS`. The
// translation file is typically the output of `scripts/pdf-to-questions.js`
// run on the localized PDF; only its `questions[i].q` and `questions[i].o`
// are read (answers and metadata are ignored).

'use strict';

const fs = require('fs');
const path = require('path');

function parseArgs(argv) {
    const args = { _: [] };
    for (let i = 0; i < argv.length; i++) {
        const a = argv[i];
        if (a === '--help' || a === '-h') args.help = true;
        else if (a.startsWith('--')) {
            const key = a.slice(2);
            const val = argv[i + 1];
            if (val === undefined || val.startsWith('--')) {
                die(`Missing value for --${key}`);
            }
            args[key] = val;
            i++;
        } else args._.push(a);
    }
    return args;
}

function die(msg, code = 1) {
    console.error('Error: ' + msg);
    process.exit(code);
}

function usage() {
    console.log(
        [
            'Usage: node scripts/merge-translations.js <target.js> --translation <path> --lang <code> [--set-name "<name>"] [--set-description "<desc>"]',
            '',
            'Injects per-question `translations.<lang> = { q, o }` into the target set,',
            'preserving the existing answers and base-language text.'
        ].join('\n')
    );
}

// Load a question-set file by requiring it with a stub `window.QUESTION_SETS`
// array; returns the most-recently pushed set.
function loadSet(filePath) {
    const abs = path.resolve(filePath);
    if (!fs.existsSync(abs)) die(`File not found: ${filePath}`);
    // Fresh registry for each load so we don't leak between calls.
    global.window = { QUESTION_SETS: [] };
    delete require.cache[abs];
    require(abs);
    const sets = global.window.QUESTION_SETS;
    if (!sets || !sets.length) die(`No QUESTION_SETS pushed by ${filePath}.`);
    return sets[sets.length - 1];
}

function jsStr(s) {
    return (
        '"' +
        String(s)
            .replace(/\\/g, '\\\\')
            .replace(/"/g, '\\"')
            .replace(/\r?\n/g, '\\n') +
        '"'
    );
}

function emitTranslations(translations) {
    // translations is { lang: { q, o } } — emit inline JSON-style.
    const langs = Object.keys(translations);
    if (!langs.length) return '';
    const parts = langs.map((lang) => {
        const t = translations[lang];
        const opts = (t.o || []).map(jsStr).join(',');
        const fields = [];
        if (typeof t.q === 'string') fields.push('q:' + jsStr(t.q));
        if (Array.isArray(t.o)) fields.push('o:[' + opts + ']');
        return jsStr(lang).replace(/"/g, '') + ':{' + fields.join(',') + '}';
    });
    // Use bareword keys for known short language codes; fallback to JSON keys.
    const body = langs
        .map((lang) => {
            const t = translations[lang];
            const opts = (t.o || []).map(jsStr).join(',');
            const fields = [];
            if (typeof t.q === 'string') fields.push('q:' + jsStr(t.q));
            if (Array.isArray(t.o)) fields.push('o:[' + opts + ']');
            return `${lang}:{${fields.join(',')}}`;
        })
        .join(',');
    return ',translations:{' + body + '}';
    // Note: `parts` was an alternative quoted-key form; we keep barewords for
    // 2-letter language codes since they are valid JS identifiers.
    void parts;
}

function emitSetTranslations(translations, indent) {
    // Set-level translations may include name/description per language.
    const langs = Object.keys(translations);
    if (!langs.length) return '';
    const inner = langs
        .map((lang) => {
            const t = translations[lang];
            const fields = [];
            if (typeof t.name === 'string')
                fields.push(`${indent}      name: ${jsStr(t.name)}`);
            if (typeof t.description === 'string')
                fields.push(
                    `${indent}      description: ${jsStr(t.description)}`
                );
            return `${indent}    ${lang}: {\n${fields.join(',\n')}\n${indent}    }`;
        })
        .join(',\n');
    return `${indent}  translations: {\n${inner}\n${indent}  },\n`;
}

function emitFile(set) {
    const lines = [];
    lines.push('// Generated by scripts/pdf-to-questions.js (base text) and');
    lines.push('// scripts/merge-translations.js (per-question translations).');
    lines.push(
        '// Format per item: { q: String, o: [a,b,c,d], a: Number (0..3),'
    );
    lines.push(
        '//   translations: { <lang>: { q: String, o: [a,b,c,d] } } }'
    );
    lines.push(
        '// The `a` (answer index) is shared across languages — answers never translate.'
    );
    lines.push('(window.QUESTION_SETS = window.QUESTION_SETS || []).push({');
    lines.push(`  id: ${jsStr(set.id)},`);
    lines.push(`  name: ${jsStr(set.name)},`);
    lines.push(`  description: ${jsStr(set.description || '')},`);
    if (set.translations && Object.keys(set.translations).length) {
        // Two-space indent for set body; emitSetTranslations expects '' as
        // its `indent` param to produce the same level.
        lines.push(emitSetTranslations(set.translations, '').replace(/\n$/, ''));
    }
    lines.push('  questions: [');
    set.questions.forEach((q, i) => {
        const opts = q.o.map(jsStr).join(',');
        const tail = emitTranslations(q.translations || {});
        const sep = i === set.questions.length - 1 ? '' : ',';
        lines.push(
            `    {q:${jsStr(q.q)},o:[${opts}],a:${q.a}${tail}}${sep}`
        );
    });
    lines.push('  ]');
    lines.push('});');
    lines.push('');
    return lines.join('\n');
}

function main() {
    const args = parseArgs(process.argv.slice(2));
    if (args.help || !args._.length) {
        usage();
        process.exit(args.help ? 0 : 1);
    }
    if (!args.translation) die('Missing --translation <path>.');
    if (!args.lang) die('Missing --lang <code>.');

    const targetPath = args._[0];
    const target = loadSet(targetPath);
    const translation = loadSet(args.translation);

    if (target.questions.length !== translation.questions.length) {
        die(
            `Question count mismatch: target has ${target.questions.length}, translation has ${translation.questions.length}.`
        );
    }

    const lang = args.lang;
    let updated = 0;
    for (let i = 0; i < target.questions.length; i++) {
        const tQ = translation.questions[i];
        target.questions[i].translations = target.questions[i].translations || {};
        target.questions[i].translations[lang] = { q: tQ.q, o: tQ.o.slice() };
        updated++;
    }

    if (args['set-name'] || args['set-description']) {
        target.translations = target.translations || {};
        target.translations[lang] = target.translations[lang] || {};
        if (args['set-name'])
            target.translations[lang].name = args['set-name'];
        if (args['set-description'])
            target.translations[lang].description = args['set-description'];
    }

    const out = emitFile(target);
    fs.writeFileSync(targetPath, out);
    console.log(
        `✔ ${targetPath}: ${updated} question translations written for "${lang}".`
    );
}

main();
